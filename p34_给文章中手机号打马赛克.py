content = """我的第一个手机号为18600037265,我的第二个手机号为13608401067,我的测试号码为29912333090"""
import re

pattern = r"(1[3-9])(\d{7})(\d{2})"
print(re.sub(pattern, r"\2**\1", content))
"""
三、re.sub() 函数解析：re.sub(pattern, repl, content)
re.sub() 是 Python 正则模块的 “替换函数”，作用是：在目标字符串中查找所有匹配pattern的子串，并用repl替换它们，返回替换后的新字符串。
参数说明：
pattern：正则表达式（这里是r"(1[3-9])\d{9}"，匹配手机号）；
repl：替换字符串（这里是r"\1*****"，表示 “第一分组内容 +*****”）；
content：被处理的原始字符串（content变量）。
四、替换规则：r"\1*****" 是什么意思？
\1：正则表达式中 “第一分组” 的引用（即(1[3-9])匹配到的内容，也就是手机号的前 2 位）；
*****：固定的替换字符，用于隐藏手机号的中间部分；
整体效果：保留手机号前 2 位，用*****替换后 9 位。
"""
