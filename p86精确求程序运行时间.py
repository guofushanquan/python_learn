import time
start=time.perf_counter()
print(start)
for i in range(3000):
    print(i)
end=time.perf_counter()
print(end-start)
"""
time.perf_counter() 是 Python 中用于高精度计时的函数，其特点如下：
返回值：一个浮点数，表示从 “性能计数器的起始点” 到当前时刻的累计时间（秒）。
精度：比 time.time() 更高，通常能精确到微秒（百万分之一秒）甚至纳秒级别，适合测量短时间运行的代码。
适用场景：专门用于测量代码片段的执行时间，不受系统时间调整（如时区变化、时钟同步）的影响，是性能测试的首选工具。
"""
