content="""
白日依2025-10-12尽,黄河入2022/10/22。
欲穷05-28-2013目,更上5/29/2020一层楼。
"""
#目标为日期格式都替换为年-月-日
import re
pattern_list=[r"(\d{4})/(\d{2})/(\d{2})",r"(\d{2})-(\d{2})-(\d{4})",r"(\d{1,2})/(\d{1,2})/(\d{4})"]
sub_list=[r"\1-\2-\3",r"\3-\1-\2",r"\3-\1-\2"]
for i in range(0,3):
   print(re.sub(pattern_list[i],sub_list[i],content))


